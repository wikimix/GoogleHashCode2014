package com.google.hashcode;

public class Street {
	
	
	private Integer streetIndex;
	
	private Integer startJunction;
	
	private Integer endJunction;
	
	private Integer beDirectional;
	
	private Integer cost;
	
	private Integer distance;

	public Integer getStreetIndex() {
		return streetIndex;
	}

	public void setStreetIndex(Integer streetIndex) {
		this.streetIndex = streetIndex;
	}

	public Integer getStartJunction() {
		return startJunction;
	}

	public void setStartJunction(Integer startJunction) {
		this.startJunction = startJunction;
	}

	public Integer getEndJunction() {
		return endJunction;
	}

	public void setEndJunction(Integer endJunction) {
		this.endJunction = endJunction;
	}

	public Integer getBeDirectional() {
		return beDirectional;
	}

	public void setBeDirectional(Integer beDirectional) {
		this.beDirectional = beDirectional;
	}

	public Integer getCost() {
		return cost;
	}

	public void setCost(Integer cost) {
		this.cost = cost;
	}

	public Integer getDistance() {
		return distance;
	}

	public void setDistance(Integer distance) {
		this.distance = distance;
	}
	
	
	
	
}
